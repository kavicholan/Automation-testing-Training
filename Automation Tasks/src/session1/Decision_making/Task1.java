//Task1: Write Java program to allow the user to input his/her age.Then the program will show if the person is eligible to vote.
// A person who is eligible to vote must be older than or equal to 18 years old.
public class Task1 {
    public static void main(String[] args) {
        int age=18;

        if(age>=18)
        {
            System.out.println("eligible to vote");
        }
        else
        {
            System.out.println("not eligible to vote");
        }


    }
}
